//--------------------------------------------------------------------------------------------------
/*
 * Service class file.
 *
 * A service is typically a Vehicule or a Building.
 */
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
/*
 * Service constructor function.
 */
//--------------------------------------------------------------------------------------------------
function Service(name, capacity, string_object, icon)
{
    this.name = name;
    this.capacity = capacity;
    this.occupancy = 0;
    this.string_object = string_object;
    this.icon = icon;
}

//--------------------------------------------------------------------------------------------------
/*
 * Add n persons to the service.
 */
//--------------------------------------------------------------------------------------------------
Service.prototype.addPersons = function(n)
{
    this.occupancy += n;
}

//--------------------------------------------------------------------------------------------------
/*
 * Remove n persons from the service.
 */
//--------------------------------------------------------------------------------------------------
Service.prototype.removePersons = function(n)
{
    this.occupancy -= n;
}

//--------------------------------------------------------------------------------------------------
/*
 * Update name string.
 */
//--------------------------------------------------------------------------------------------------
Service.prototype.Update_String = function()
{
    this.string_object.innerHTML =     this.name
                    +":\t"
                    +this.occupancy
                    +"/"
                    +this.capacity;
                
}

